function searchTable() {
  // Obtain the filter string from the user input field
  let filter = document.getElementById('searchInput1').value.toUpperCase();

  // Obtain the table from the DOM
  let table = document.getElementById('data-table');

  // Iterate through each row in the table
  for (let row of table.rows) {
      // Skip the header row (the first row)
      if (row.rowIndex === 0) continue;

    // Set the initial display of each row to "none"
    row.style.display = 'none';

    // Iterate through each cell in the row
    for (let cell of row.cells) {
      // Check if the cell contains the filter string
      if (cell.textContent.toUpperCase().indexOf(filter) > -1) {
        // If so, show the row by setting its display to "table-row"
        row.style.display = 'table-row';

        break;
      }
    }
  }
}

function generateTable(items) {
    let tbody = document.getElementById("data-table").getElementsByTagName("tbody")[0];
    let tr, td;

    // Empty the table body
    tbody.innerHTML = "";

    // Check the number of keys in the first object of the items array
    console.log(Object.keys(items[0]).length);

    // Iterate through the properties of the waveData object
    for (let key in items[0].waveData) {
        // Create a new row
        tr = document.createElement("tr");

		// Add a new cell for the counter
        td = document.createElement("td");
        td.textContent = key;
        tr.appendChild(td);

        // Create and append cells for Monsters
        td = document.createElement("td");
        monsterNames = Object.keys(items[0].waveData[key]);
        monsterNames.forEach((monster) => {
            let monsterSpan = document.createElement("span");
            monsterSpan.innerHTML = `${monster}<br>`;
            td.appendChild(monsterSpan);
        });
        tr.appendChild(td);

        // Create and append cells for Numbers
        td = document.createElement("td");
        numbers = Object.values(items[0].waveData[key]);
        numbers.forEach((number) => {
            let numberSpan = document.createElement("span");
            numberSpan.innerHTML = `${number}<br>`;
            td.appendChild(numberSpan);
        });
        tr.appendChild(td);

        // Append the row to the table body
        tbody.appendChild(tr);
    }
}

